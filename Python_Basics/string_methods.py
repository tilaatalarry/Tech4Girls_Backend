# Demonstrating various string methods in Python

# Example string
original_string = "Hello World!"

# Using upper() method
print("Uppercase:", original_string.upper())

# Using lower() method
print("Lowercase:", original_string.lower())

# Using replace() method
print("Replaced:", original_string.replace("World", "Hamdallah"))

# Using join() method
words = ["Hello", "Hamdallah", "World"]
print("Joined:", " ".join(words))
