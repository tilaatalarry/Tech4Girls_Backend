# Define two variables x and y with arbitrary values
x = 3
y = 5

# Perform calculations
addition = x + y
subtraction = x - y
multiplication = x * y
division = x / y
floor_division = x // y
exponentiation = x ** y
modulus = x % y

# Perform comparisons
equal = x == y
not_equal = x != y
less_than = x < y
greater_than = x > y
less_than_equal = x <= y
greater_than_equal = x >= y

print(f"Addition: {x} + {y} = {addition}")
print(f"Subtraction: {x} - {y} = {subtraction}")
print(f"Multiplication: {x} * {y} = {multiplication}")
print(f"Division: {x} / {y} = {division}")
print(f"Floor Division: {x} // {y} = {floor_division}")
print(f"Exponentiation: {x} ** {y} = {exponentiation}")
print(f"Modulus: {x} % {y} = {modulus}")

print(f"Equal: {x} == {y} -> {equal}")
print(f"Not Equal: {x} != {y} -> {not_equal}")
print(f"Less Than: {x} < {y} -> {less_than}")
print(f"Greater Than: {x} > {y} -> {greater_than}")
print(f"Less Than or Equal: {x} <= {y} -> {less_than_equal}")
print(f"Greater Than or Equal: {x} >= {y} -> {greater_than_equal}")
